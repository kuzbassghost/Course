x=30
y=20
print("x = ", x)
print("y = ", y)

# Основные математические операции
print("x + y =", x+y)
print("x - y =", x-y)
print("x * y =", x*y)
print("x / y =", x/y)
print("x % y =", x%y)   # остаток от деления
print("x ** y =", x**y) # возведение в степень
print("(3+2)*6 =", (3+2)*6)

#битовые операции
x=5
y=20
print("x =", x, "=", bin(x))
print("y =", y, "=", hex(y))
print("20 =", hex(20))
print("20 =", oct(20))
print("e =", y, "=", hex(y))
print("------------------------")
print("x =", x, "=", bin(x))
print("y =", y, "=", bin(y))
print("x | y", "=", bin(x|y)) # Побитовое ИЛИ
print("x & y", "=", bin(x&y)) # Побитовое И
print("~ x", "=", bin(~x))  # отрицание
print("x  << 1 =", bin(x<<1)) # Побитовый сдвиг влево  на 1 бит - умножене
print("x  << 1 =", bin(x<<1)) # Побитовый сдвиг вправо на 1 бит - деление


